package hellojava;

public class VariablesExercise {
    public static void main(String args[]) {
        String name;
        int year;
        int month;
        int day;
        boolean married;
        
        name = "코드잇";
        year = 2015;
        month = 7;
        day = 20;
        married = false;
        
       System.out.println("이름 : " + name);
       System.out.println("생일 : " + year + "년 " + month + "월 " + day + "일");
       System.out.println("결혼 : " + married);
    }
}


